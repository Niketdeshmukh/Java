import java.io.*;

class multiply {
    public static void main(String[] args)
    {

        // f is to ensures that numbers are float DATA TYPE
        float f1 = 1.5f;
        float f2 = 2.0f;

        // to store the multiplied value
        float p = f1 * f2;

        // to print the product
        System.out.println("The product is: " + p);
    }
}